package app.model;

public class MainClass {
	private int mainId;
	private String name;
	private int catId;
	/**
	 * mainIdを取得します。
	 * @return mainId
	 */
	public int getMainId() {
	    return mainId;
	}
	/**
	 * mainIdを設定します。
	 * @param mainId mainId
	 */
	public void setMainId(int mainId) {
	    this.mainId = mainId;
	}
	/**
	 * nameを取得します。
	 * @return name
	 */
	public String getName() {
	    return name;
	}
	/**
	 * nameを設定します。
	 * @param name name
	 */
	public void setName(String name) {
	    this.name = name;
	}
	/**
	 * catIdを取得します。
	 * @return catId
	 */
	public int getCatId() {
	    return catId;
	}
	/**
	 * catIdを設定します。
	 * @param catId catId
	 */
	public void setCatId(int catId) {
	    this.catId = catId;
	}
}
